export function Icon({ name, className = 'icon' }: { name: string; className?: string }) {
  const path = PATHS[name];
  if (!path) return null;
  return (
    <svg className={className} viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth={2} strokeLinecap="round" strokeLinejoin="round">
      {path}
    </svg>
  );
}

export function initials(str?: string | null): string {
  const s = String(str || '').trim();
  if (!s) return '?';
  const p = s.split(/\s+/).filter(Boolean);
  const out = p.length === 1 ? p[0].slice(0, 2) : p[0][0] + p[p.length - 1][0];
  return out.toUpperCase();
}

export function Avatar({ name, variant = '' }: { name?: string | null; variant?: '' | 'green' }) {
  return <div className={`row-avatar ${variant}`}>{initials(name)}</div>;
}

// Big admin label + small original Telegram name underneath
export function NameBlock({ display, original, hint }: { display: string; original?: string | null; hint?: boolean }) {
  const labeled = !!original && display.trim().toLowerCase() !== String(original).trim().toLowerCase();
  return (
    <>
      <div className="entity-name">{display}</div>
      {labeled ? (
        <div className="entity-original" title="Telegram name"><span className="og-tag">TG</span>{original}</div>
      ) : hint ? (
        <div className="entity-original"><span className="og-tag muted">no label</span></div>
      ) : null}
    </>
  );
}
